//Write a C++ Program to find the product of two numbers entered by the User

#include<iostream>
using namespace std;

int main()
{
    float a,b,product;
    cout<<"\n Enter the First Value -> ";
    cin>>a;
    cout<<"\n Enter the Second Value -> ";
    cin>>b;
    product=a*b;
    cout<<"\n The Final Answer is -> "<<product<<endl;
    return 0;
}
